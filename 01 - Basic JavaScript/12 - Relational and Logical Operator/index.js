// Relational operator 
let num1 = 10;
let num2 = 20;

console.log(num1 > num2); //false
console.log(num1 < num2); //true
console.log(num1 >= num2); //false
console.log(num1 <= num2); //true
console.log(5 == 5); // true -> it will check the value
console.log(5 == '5'); // true -> it will check the value
console.log(5 === '5'); // false -> it will check the data type
console.log(5 == !5); // false


// Logical Operator 
let a = 5;
let b = 10;
let c = 15;

console.log(c > a && c > b) //true
console.log(c > a || c > b) //true
console.log(!true) //false
console.log(!false) //true
console.log(!(a > b)) //true