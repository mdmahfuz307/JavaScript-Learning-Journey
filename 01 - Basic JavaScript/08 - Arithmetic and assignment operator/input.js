// Arithmetic Operator
let num1 = 10;
let num2 = 5;

let sum = num1 + num2;
console.log("Sum of two numbers: ", sum);

let sub = num1 - num2;
console.log("Sub of two numbers: ", sub);

let multi = num1 * num2;
console.log("Multiply of two numbers: ", multi);

let divide = num1 / num2;
console.log("Division of two numbers: ", divide);

let pow = num1 ** num2;
console.log("Power of two numbers: ", pow);

// Increment and Decrement Operator
let x = 10;
let result1 = x++;
console.log(result1);

let y = 10;
let result2 = y--;
console.log(result2);


// Assignment Operator
let a = 20;
a += 5; //25
console.log(a);
a -= 5; //20
console.log(a);